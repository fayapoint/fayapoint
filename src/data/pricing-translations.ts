export function getPricingTranslation(text: string, locale: string): string {
  if (locale !== "pt" && locale !== "pt-BR") return text;
  
  // Direct match
  if (pricingTranslations[text]?.pt) {
    return pricingTranslations[text].pt;
  }
  
  return text;
}

export function getPricingDescriptionTranslation(text: string, unitLabel: string, locale: string): string {
  if (locale !== "pt" && locale !== "pt-BR") return text;

  if (pricingTranslations[unitLabel]?.description_pt) {
    return pricingTranslations[unitLabel].description_pt;
  }
  
  return text;
}
